import msi.gama.precompiler.GamlAnnotations.action;
import msi.gama.precompiler.GamlAnnotations.args;
import msi.gama.precompiler.GamlAnnotations.skill;
import msi.gama.runtime.IScope;
import msi.gama.runtime.exceptions.GamaRuntimeException;
import msi.gaml.skills.Skill;

/**
 * MAELIASkill: This class is intended to define additional actions for MAELIA dedicated agents.
 * 
 * @author Benoit Gaudou 17 jan. 12
 */

@skill(name = "MAELIA", concept = { IConcept.SKILL })
public class MAELIASkill extends Skill {

	@action(name = "maeliaTimeStamp")
	@args(names = {})
	public Long maeliaTimeStamp(final IScope scope) throws GamaRuntimeException {
		Long timeStamp = System.currentTimeMillis();
		return timeStamp;
	}

}
